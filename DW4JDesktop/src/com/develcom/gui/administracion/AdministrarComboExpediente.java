    /**
     * Llena la lista desplegables de los indices Combos
     *
     * @param cat Nomnre de la Categoria
     * @param idCat El id de la Categoria
     */
    private void llenarComboIndice(String cat, int idCat) {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        try {
            model.addElement("");

            traza.trace("buscando los indices de la categoria: " + cat, Level.INFO);
            traza.trace("id de la categoria: " + idCat, Level.INFO);
            
            if(indices != null){
                indices.clear();
            }

            indices = new AdministracionBusqueda().buscandoIndices(idCat);
            
            if(!argCombos.isEmpty()){
                argCombos.clear();
            }
            
            if (!indices.isEmpty()) {
                for (Indice arg : indices) {
                    if (arg.getTipo().equalsIgnoreCase("COMBO")) {
                        model.addElement(arg.getIndice());
                        argCombos.add(arg);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "La Categoria " + cat + "\nno tiene √çndices", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

            traza.trace("tama√±o de modelo para combos " + model.getSize(), Level.INFO);

            if (model.getSize() == 1) {
                JOptionPane.showMessageDialog(this, "La Categoria " + cat + "\nno tiene √çndices asociados de Listas Desplegable", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                cmbIndice.setModel(model);
            }
            
        } catch (SOAPException ex) {
            traza.trace("error soap en el webservice", Level.ERROR, ex);
        } catch (SOAPFaultException ex) {
            traza.trace("error soapfault en el webservice", Level.ERROR, ex);
        }
    }

